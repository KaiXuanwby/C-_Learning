//赋值运算符：
//1. =：将右边的值赋给左边的变量
//2. +=：将右边的值与左边的变量相加，并将结果赋给左边的变量
//3. -=：将右边的值从左边的变量中减去，并将结果赋给左边的变量
//4. *=：将右边的值与左边的变量相乘，并将结果赋给左边的变量
//5. /=：将左边的变量除以右边的值，并将结果赋给左边的变量
//6. %=：将左边的变量除以右边的值，取余数，并将结果赋给左边的变量
//x7. &=：将右边的值与左边的变量进行按位与运算，并将结果赋给左边的变量
//x8. |=：将右边的值与左边的变量进行按位或运算，并将结果赋给左边的变量
//x9. ^=：将右边的值与左边的变量进行按位异或运算，并将结果赋给左边的变量
//x10. <<=：将左边的变量左移右边的值次，并将结果赋给左边的变量
//x11. >>=：将左边的变量右移右边的值次，并将结果赋给左边的变量

#include<iostream>
using namespace std;

int main15()
{
	int a = 10;
	a += 2;   //  a = a+2
	cout << "a = " << a << endl;

	a = 10;
	a *= 2;   //a = a*2
	cout << "a = " << a << endl;

	a = 10;
	a /= 2;   //a = a/2
	cout << "a = " << a << endl;

	a = 10;
	a %= 2;   //a = a%2
	cout << "a = " << a << endl;
	system("pause");
	return 0;
}